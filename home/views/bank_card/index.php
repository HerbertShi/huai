            <h1>（一）产品定义</h1>
            <p>圆鼎借记卡是江苏省农村信用社发行的圆鼎卡系列卡种之一，是由江苏省（市、区）农村信用合作联社（包括农村合作银行、农村商业银行）委托江苏省农村信用社联合社向银监会统一申请的银行卡品牌。圆鼎借记卡为人民币磁条卡，分个人卡和单位卡两种。各委托单位为发卡机构，持卡客户必须先存入资金后取款、消费，不具有透支功能。</p>
            <div class="margin_t2">
              <h1>（二）主要功能</h1>
              <ul>
                <li>1、可在系统内柜面、自动存取款机等渠道办理存款、取款、转账结算、查询等业务。</li>
                <li>2、可通过系统内网上银行、手机银行、电话银行办理转账结算、查询等业务。</li>
                <li>3、可在他行自助存取款机上办理取款、查询业务。（在苏南未入网8家的自助存取款机上办理取款业务视同委托他行，客户需要支付相应手续费）</li>
                <li>4、可在布放具有银联标识的商户POS上办理查询、消费、预授权、预授权完成等业务。</li>
                <li>5、可在江苏省内兴业银行、工商银行柜面办理存取款、转账等业务。</li>
                <li>6、可在信用社柜面办理银证转账业务。</li>
                <li>7、可在苏南未入网机构柜面办理存取款、转账等业务。</li>
                <li>8、可在受理农民工卡的他行柜面办理取款、查询业务。</li>
                <li>9、可在已加入农信银清算系统的其他省市农村信用社柜面办理农信银通存通兑业务。</li>
              </ul>
            </div>
            <div class="margin_t2">
              <h1>（三）产品特点</h1>
              <ul>
                <li>1、具有存取款、转账等结算功能特点。</li>
                <li>2、可在特约商户消费、预授权、预授权完成等。</li>
                <li>3、安全、方便、快捷、可在柜面、自助设备等多个渠道办理业务</li>
                <li>4、先存资金后使用原则，不具有透支功能。</li>
                <li>5、单位卡不可存取现金。</li>
                <li>6、可在境内和境外使用，以人民币进行结算</li>
              </ul>
